// Tipo de dato indefinido -> al no asiganr un valor esta variable tine un valor undefined
let indefinido;
console.log(indefinido);

// Tipo cadena de caracter
let texto = 'Cadena de Texto'
console.log(texto + ":  Tipo de Dato-> " + typeof(texto));

// Tipo entero
let entero = 30;
console.log(entero +":  Tipo de Dato-> "+ typeof(entero));

// Tipo Decimal
let decimal = 19.75
console.log(decimal + ": Tipo de Dato->" + typeof(decimal));

// Tipos de Datos Booleanos
console.log(typeof(true));
console.log(typeof(false));